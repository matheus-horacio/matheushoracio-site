// ==================== FUN√á√ÉO DE TRADU√á√ÉO (CORRIGIDA) ====================
function translatePage(lang) {
  // Traduzir todos os elementos com data-i18n
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      // Se cont√©m HTML (tags), usar innerHTML
      if (translations[lang][key].includes('<')) {
        el.innerHTML = translations[lang][key];
      } else {
        el.textContent = translations[lang][key];
      }
    }
  });
  
  // Atualizar o t√≠tulo da p√°gina
  if (translations[lang] && translations[lang]['page-title']) {
    document.title = translations[lang]['page-title'];
  }
  
  // Atualizar atributo lang do HTML
  document.documentElement.lang = lang === 'pt' ? 'pt-br' : lang;
  
  // Salvar prefer√™ncia
  localStorage.setItem('preferred-language', lang);
  
  // Atualizar selecionado no dropdown
  document.querySelectorAll('.language-option').forEach(opt => {
    opt.classList.toggle('selected', opt.getAttribute('data-lang') === lang);
  });
}
